export interface FieldIds {
  /**
   * The unique ID for the form control element (e.g., input, select).
   */
  controlId: string;

  /**
   * The unique ID for the label element associated with the form control.
   */
  labelId: string;

  /**
   * The unique ID for the description element providing additional information about the form control.
   */
  descriptionId: string;

  /**
   * The unique ID for the error message element related to the form control.
   */
  errorId: string;
}
